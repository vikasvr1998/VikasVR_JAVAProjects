package Java22July2024;

public class forLoop7
{
    public static void main(String[] args)
    {
        for (int i=0; i<=10 ; i++)
        {
            if (i%3==0)
            {
                System.out.println(i+" Divisible by 3");
            }
            else
                System.out.println(i);
        }
    }
}