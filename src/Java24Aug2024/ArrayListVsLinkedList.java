public class ArrayListVsLinkedList
{
    /*
      ArrayList :
      1. Finding an element is very easy (Here we can directly get access to data by knowing their index)
      2. Not that easy to add/delete the elements
      3. Consumes less memory unlike Linked List
      4. Best for storing and accessing the data

     Linked List :
     1. Very difficult to find the element in case of Linked List (Because it in not stored like continuous memory)
     2. Adding and Deleting the elements is very easy .
     3. Consumes a lot of memory
     4. Better for manipulating the data (Where changes on data like adding/deleting the data are done frequently)


     Note : Vector is an ArrayList with thread safety

     */
}
