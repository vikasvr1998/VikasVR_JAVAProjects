package Java11Aug2024.Interface;

public class abstractVsInterface
{
    /*

    Abstract :
    1. Don't support multiple inheritance
    2. Can have private , protected and public members
    3. Can have non-static  and non-final members
    4. Constructors can be used

    Interface :
    1. Supports multiple inheritance
    2. All members are public
    3. All members are static & final
    4. No Constructors allowed

   */
}
