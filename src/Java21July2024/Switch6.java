package Java21July2024;

public class Switch6
{
    public static void main(String[] args)
    {
        int a=-1;
        switch (a)
        {
            default :
                System.out.println("Invalid");
                break;
            case -1:
                System.out.println("TEN");
                break;
            case 99:
                System.out.println("100");
        }
    }
}
