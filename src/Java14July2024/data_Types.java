package Java14July2024;

public class data_Types
{
    public static void main(String[] args)
    {
        System.out.println("Welcome to Data Types");
        byte b=10;
        short s=12;
        char c='A';
        int i=100;
        long l=9876543210L; // Note : For Long data type you should compulsory end the value by 'l' or 'L'
        float f=3.14f; // Same thing applies to float also
        double d=3.14234556677;
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
    }
}